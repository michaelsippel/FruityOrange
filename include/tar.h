#ifndef _TAR_H
#define _TAR_H

#include <stdint.h>

#define TAR_SIZE 512

#define TAR_TYPE_REG  0
#define TAR_TYPE_LINK 1
#define TAR_TYPE_SYM  2

typedef struct tar_header {
  char name[100];
  char mode[8];
  char uid[8];
  char gid[8];
  char size[12];
  char mtime[12];
  char chksum[8];
  char typeflag;
  char linkname[100];
  char magic[6];
  char version[2];
  char uname[32];
  char gname[32];
  char devmajor[8];
  char devminor[8];
  char prefix[155];
} tar_header_t;

int tar_getsize(const char *in);
int tar_get_num_entries(void *address);

#endif

